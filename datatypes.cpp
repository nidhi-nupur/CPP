#include<iostream>
using namespace std;
int main() {
    int a = 12;
    float b = 24.64;
    char c = '@';
    string d = "Nidhi";
    cout << a << "  " << b << "  " << c << "  " << d << endl;
    cout << sizeof(int) << " Bytes" << endl;
    cout << sizeof(float) << " Bytes" << endl;
    cout << sizeof(char) << " Bytes" << endl;
    cout << sizeof(string) << " Bytes" << endl;
    cout << sizeof(long int) << " Bytes" << endl;
    cout << sizeof(short int) << " Bytes" << endl;
    cout << sizeof(double) << " Bytes" << endl;
    cout << sizeof(signed int) << " Bytes" << endl;
    cout << sizeof(unsigned int) << " Bytes" << endl;
    cout << sizeof(signed char) << " Bytes" << endl;
    cout << sizeof(unsigned char) << " Bytes" << endl;
    cout << sizeof(bool) << " Bytes" << endl;
    cout << sizeof(long double) << " Bytes" << endl;
}